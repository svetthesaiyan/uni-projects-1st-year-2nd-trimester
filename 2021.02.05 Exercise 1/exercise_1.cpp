/* Да се напише програма, която обработва броя на ваксините, произведени от фирма за дните от 1 до 31 декември 2020, по следния начин:
 * а) въвежда броя на ваксините, произведени от фирмата за всеки един ден от 1.12.2020г до 31.12.2020г  включително - стойности до 5000. (случайни числа)
 * б) извежда колко (като брой) са дните, през които са произведени по по‐малко от 3000 ваксини на ден;
 * в) извежда минималния брой ваксини, произведени от фирмата за един ден;
 */

// масив от 31 цели числа- от 0 до 30 или 1 до 31...
