// От клавиатурата се въвеждат цели числа до въвеждането на число, кратно (дели се без остатък) на 13. Да се намери сумата на въведените числа.
#include <iostream>
using namespace std;
int main()
{
	int s=0, k; // s е сумата, а k е числото, което въвеждаме

	do
	{
		cout<<"Enter a number: ";	cin>>k;
		s+=k; // увеличаваме сумата с числото
	}
	while(k%13!=0);
	cout<<"Sum= "<<s<<endl;
	return 0;
}
